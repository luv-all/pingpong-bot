//! 경진용 `Arm::competition` 기하/관절 한계.
//!
//! DOF: yaw + 어깨 + 팔꿈치 + 손목 — `all-4-export.urdf` 직렬 체인과 동일.
//! competition primitive는 URDF origin 합산 길이를 쓴다 (≈0.07~0.15 m/세그먼트).

/// 베이스 y [m] — 탁구대 로봇쪽 끝(y=0) 기준. 음수면 테이블 바깥.
///
/// primitive `Arm::competition()`과 URDF 카탈로그 로봇(`SimRobotMount::
/// rep103_z_up_at_table_end`)이 **둘 다** 이 값을 쓴다 — 같은 실물 로봇의
/// 두 모델이라 마운트가 어긋나면 안 된다(`competition_primitive_matches_
/// simplified_4dof_urdf_chain`이 이 일치를 지킨다).
///
/// 이전 `+0.02`(테이블 끝에서 살짝 **안쪽**)에서 옮겼다. 근거(2026-07-23,
/// `tools/shot_tune` 실제 Rapier 랠리 성공률, 시드 8종 × 12발 = 96발):
/// - `+0.02, height 0.0`: 최고 **75/96**, 슈터 설정 한 칸 차이로 **0/96**까지
///   무너지는 칼날 같은 능선.
/// - `-0.02, height 0.05`: 여러 슈터 설정에서 **96/96**, 넓은 고원.
///
/// 고원(`base_y` -0.10~-0.02)에서 실기 배치를 가장 덜 바꾸는 끝을 골랐다.
pub const BASE_Y: f64 = -0.02;

/// 베이스의 탁구대 면(`table::SURFACE_Z`) 대비 높이 오프셋 [m].
///
/// 실기 AXL 레일 브래킷이 테이블 면보다 약 3cm 위라는 실측 보고가 있는데,
/// 스윕 최적 고원(0.05~0.10)이 그와 겹친다 — 이전 `0.0`(테이블 면과 완전
/// 동일 높이)이 오히려 실기와 다른 단순화였다. 고원 중 실기 보고값에 가장
/// 가까운 0.05를 택했다.
///
/// ⚠️ [`BASE_Y`]와 함께 **실기 하드웨어 배치**를 뜻한다. sim만 바꾸고 실기를
/// 두면 둘이 어긋난다 — 실기 적용 여부는 사람이 판단할 것.
pub const MOUNT_HEIGHT_OFFSET_M: f64 = 0.05;

/// 4-DOF 휴지(ready) 자세 [rad] — yaw, 어깨, 팔꿈치, 손목 순.
///
/// **왜 관절 한계 중점(예전 값)이 아닌가**: 스윙 커밋 실패의 실제 병목은
/// 임팩트 자세의 도달성이 아니라 *휴지 자세 → 임팩트 자세* 관절공간 이동에
/// 걸리는 시간이다(2026-07-23 2차 조사, `.omc/research/
/// known-regressions-realistic-joint-speed.md` §1). 재보정된 관절속도
/// (~2.88 rad/s) 아래서 quintic 소요시간은 **가장 많이 움직이는 한 관절**이
/// 결정하므로, 휴지 자세는 "중립적으로 보이는" 곳이 아니라 *실제로 마주칠
/// 임팩트 자세들까지의 최악 이동거리가 최소*인 곳이어야 한다.
///
/// 값 산출(`cargo run -p shot-tune --release -- --robot 4-dof --rest-pose-search`):
/// 테이블 폭 전역(x 10~90%) × 접수 창(y 0.20~0.55) × 실현가능 높이 대역
/// (테이블 위 10~30cm) × 대표 입사속도 3종 = 240 시나리오 중 IK 해가 있는
/// 165개의 임팩트 자세를 모아, 관절마다 그 각도 구간의 중점(1D Chebyshev
/// 중심)을 취했다. 비용 `max_시나리오 max_관절 |Δq|`는 두 max가 교환 가능해
/// 관절별로 분리되므로 이 중점이 **정확한** minimax 최적해다(근사가 아님).
/// 평균이 아니라 최악값 기준인 이유: 나머지 관절이 아무리 가까워도 한
/// 관절이 멀면 그 관절이 소요시간을 지배한다.
///
/// 효과(실측): 최악 Δq 2.00 rad → **1.183 rad**, 필요시간 1.30s → **0.770s**.
/// 이것만으로는 commit 창(0.125~0.175s)에 못 들어와, rough 단계 관절
/// 선추종(`plan_coarse_track` + `RobotState::set_targets`)과 **함께** 쓴다.
pub const READY_JOINTS_4DOF: [f64; 4] = [0.1207, 0.0, 0.1719, -0.6756];

// 관절 추종 최대 각속도는 여기 없다 — 근거 없는 리터럴(`16.0`)이었던 것을 제거했다.
// 실기 Dynamixel 스펙 기반 SSOT는
// `crate::hardware::dynamixel::DYNAMIXEL_MAX_JOINT_SPEED_RAD_S`
// (`Arm::competition()`과 URDF 카탈로그 모두 공유; 근거: `.omc/research/dynamixel-specs.md`).

/// 리니어 레일 최대 속도 [m/s] (시뮬).
///
/// 이전 `12.0`은 근거 없는 리터럴이었다 — 테이블 전폭(1.525 m)을 0.127초에
/// 주파해, rough-to-fine 추종이 예측 방향으로 레일을 미리 옮길 때 렌더
/// 프레임 상 순간이동처럼 보였다(육안 확인, 2026-07-23). 실기
/// `config/real-hardware.toml`의 `[hardware.rail]` `vel`/`max_vel` = 5.0 m/s에
/// 맞춰 재보정 — 전폭 주파 0.305초로, 연속적인 움직임으로 보인다.
pub const RAIL_MAX_SPEED: f64 = 5.0;
